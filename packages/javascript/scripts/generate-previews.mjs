// Builds src/generated/previews.ts by inlining the HTML files in
// spec/previews/layouts/* and the matching index.json.
//
// Run via `pnpm generate` (wired in package.json after the schema/catalog gen).

import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const packageRoot = path.resolve(__dirname, "..");
const repoRoot = path.resolve(packageRoot, "../..");
const previewsDir = path.join(repoRoot, "spec/previews/layouts");
const generatedRoot = path.join(packageRoot, "src/generated");

await fs.mkdir(generatedRoot, { recursive: true });

const indexJson = JSON.parse(
  await fs.readFile(path.join(previewsDir, "index.json"), "utf8"),
);

const previews = {};
for (const record of indexJson.records) {
  const html = await fs.readFile(path.join(previewsDir, record.file), "utf8");
  // Strip the trailing newline the renderer adds so the JS string is clean.
  previews[record.id] = html.replace(/\n$/, "");
}

const banner = `// Generated by scripts/generate-previews.mjs from spec/previews/layouts/*. Do not edit by hand.\n\n`;

const indexLiteral = JSON.stringify(indexJson, null, 2);
const previewsLiteral = JSON.stringify(previews, null, 2);

const source =
  banner +
  `export const layoutPreviewsRaw: Record<string, string> = ${previewsLiteral} as const satisfies Record<string, string>;\n\n` +
  `export const layoutPreviewIndexRaw = ${indexLiteral} as const;\n`;

await fs.writeFile(path.join(generatedRoot, "previews.ts"), source, "utf8");

console.log(
  `Generated src/generated/previews.ts (${indexJson.records.length} previews)`,
);
